    private void preencherComboTutores() {
        ArrayList<Animal> animais = telaPrincipal.getSistema().getAnimais();
        ArrayList<Veterinario> vets = telaPrincipal.getSistema().getVeterinarios();

        // Cria um modelo de String e popula com os nomes
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (Animal a : animais) {
            model.addElement(a.getNome());
        }
        
        DefaultComboBoxModel<String> model2 = new DefaultComboBoxModel<>();
        for (Veterinario v : vets) {
            model2.addElement(v.getNome());
        }

        // Atualiza o combo
        cxAnimal.setModel(model);
        cxVet.setModel(model2);
    }

