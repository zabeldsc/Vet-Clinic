    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         // Verifica se um animal foi selecionado
        if (cxAnimal.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Selecione um animal.");
            return;
        }

        // Verifica se um veterinário foi selecionado
        if (cxVet.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Selecione um veterinário.");
            return;
        }

        // Verifica se problema foi preenchido
        if (cxProblema.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo de problema.");
            return;
        }

        // Verifica se diagnóstico foi preenchido
        if (cxDiagnostico.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo de diagnóstico.");
            return;
        }

        // Verifica se dia, mês e ano foram preenchidos e são válidos
        String diaStr = cxDia.getText().trim();
        String mesStr = cxMes.getText().trim();
        String anoStr = cxAno.getText().trim();

        if (diaStr.isEmpty() || mesStr.isEmpty() || anoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha dia, mês e ano.");
            return;
        }

        int dia, mes, ano;
        try {
            dia = Integer.parseInt(diaStr);
            mes = Integer.parseInt(mesStr);
            ano = Integer.parseInt(anoStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Data inválida: apenas números.");
            return;
        }

        // Validação de data real
        try {
            LocalDate.of(ano, mes, dia);
        } catch (DateTimeException e) {
            JOptionPane.showMessageDialog(this, "Data inválida: " + e.getMessage());
            return;
        }

        // Se chegou aqui, todos os dados estão válidos
        JOptionPane.showMessageDialog(this, "Dados válidos!");
        
        // Recupera os valores dos campos
        String problema = cxProblema.getText().trim();
        String diagnostico = cxDiagnostico.getText().trim();
        String medicamentos = cxMedicamento.getText().trim();
        LocalDate data = LocalDate.of(ano, mes, dia);
        
        String nomeSelecionado = (String) cxVet.getSelectedItem();

        Veterinario vet = null;
        for (Veterinario v : telaPrincipal.getSistema().getVeterinarios()) {
            if (v.getNome().equals(nomeSelecionado)) {
                vet = v;
                break;
            }
        }

        if (vet == null) {
            JOptionPane.showMessageDialog(this, "Veterinário não encontrado.");
            return;
        }


        // Cria a consulta
        Consulta novaConsulta = new Consulta(problema, diagnostico, medicamentos, data, vet);

       String nomeSelecionad = (String) cxAnimal.getSelectedItem();

        Animal animal = null;
        for (Animal a : telaPrincipal.getSistema().getAnimais()) {
            if (a.getNome().equals(nomeSelecionad)) {
                animal = a;
                break;
            }
        }

        if (animal == null) {
            JOptionPane.showMessageDialog(this, "Animal não encontrado.");
            return;
        }

        animal.adicionarConsulta(novaConsulta);
        
        cxMedicamento.setText("");
        cxDiagnostico.setText("");
        cxProblema.setText("");
        cxDia.setText("");
        cxMes.setText("");
        cxAno.setText("");
        cxAnimal.setSelectedIndex(-1);
        cxVet.setSelectedIndex(-1);
        
        telaPrincipal.mostrarTela("telaMenu");
    }//GEN-LAST:event_jButton1ActionPerformed

